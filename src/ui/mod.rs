//! 用户界面模块
//!
//! 包含合成器自带的各类用户界面组件，
//! 这些组件在Wayland客户端之外提供原生交互体验

/// 配置错误通知组件
///
/// 功能：
/// 当配置文件出现错误时，在屏幕上显示通知
///
/// 在合成器中的作用：
/// 1. 实时反馈配置问题
/// 2. 避免因配置错误导致功能异常
/// 3. 指导用户修复配置
pub mod config_error_notification;

/// 快捷键提示覆盖层组件
///
/// 功能：
/// 显示当前可用的快捷键列表
///
/// 在合成器中的作用：
/// 1. 提高用户操作效率
/// 2. 降低学习曲线
/// 3. 辅助用户发现高级功能
pub mod hotkey_overlay;

/// 屏幕过渡动画组件
///
/// 功能：
/// 管理屏幕之间的切换动画
///
/// 在合成器中的作用：
/// 1. 提供视觉连续性
/// 2. 增强用户体验
/// 3. 平滑处理屏幕分辨率/布局变化
pub mod screen_transition;